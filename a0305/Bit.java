package a0305;

public class Bit {
    public static void main(String[] args) {
        System.out.println("45 & 25 = " + (45 & 25));
        System.out.println("45 | 25 = " + (45 | 25));
		System.out.println("45 ^ 25 = " + (45 ^ 25)); //서로 다를때 1
		System.out.println("~45 = " + (~45));
		System.out.println("-------------------------------");
    }
}
